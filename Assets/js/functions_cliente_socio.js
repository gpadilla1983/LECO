async function EditarClienteSocio(id_cliente_socio) {
  if ($("#ajax-content").length) {
    $("#ajax-content").remove();
  }

 //debugger;

  var ajaxcontent = document.createElement("div");

  ajaxcontent.setAttribute("id", "ajax-content");

  
  var html = await $.ajax({
    type: "GET",
    beforeSend: function () {
      $("body").dynamicSpinner({
        loadingText: "Cargando...",
      });
    },

    url: base_url + "ModalClienteSocio/" + id_cliente_socio,
  });

  ajaxcontent.innerHTML += html;

  $("#cuerpo-content").append(ajaxcontent);

  $.ajax({
    type: "GET",
    async: true,
    url: base_url + "relClienteSocio/getClienteSocio/" + id_cliente_socio,
    success: function (respuesta) {
      $("body").dynamicSpinnerDestroy({});
      var datos = JSON.parse(respuesta);


        //console.log(datos.id_cliente);
        //console.log(datos.id_socio);
        //console.log(datos.id_estado_logico);

        $("#tbx_cliente").val(datos.datos_cliente);
        $("#tbx_socio").val(datos.datos_socio);
        $("#ddl_estatus_cliente_socio").val(datos.id_estado_logico);
        $("#div_estatus").show();
        $("#modalDatosClienteSocio").modal("show");
    },
  });
}


function AgregarActualizarClienteSocio(id_cliente_socio) {

  $.ajax({
    type: "POST",
    async: true,
    beforeSend: function () {
      $("body").dynamicSpinner({
        loadingText: "Cargando...",
      });
    },
    
    url: base_url + "relClienteSocio/AgregarActualizarClienteSocio/" + id_cliente_socio,
    data: $("#formModalClienteSocio").serialize(),
    success: function (respuesta) {
      $("body").dynamicSpinnerDestroy({});
      $("#modalDatosClienteSocio").modal("hide");  
      if (respuesta == 1) {
        swal("Relación Cliente - Socio actualizada con éxito", "", "success");
        btn_consultarClick();
      } else {
        if (respuesta == 0) {
          swal("Error", "Ha ocurrido un error", "error");
        } else {
          swal("Error", respuesta, "error");
        }
      }
    },
  });

  return false;
}

